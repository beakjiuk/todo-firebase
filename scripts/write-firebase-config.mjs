/**
 * Vercel / CI: reads env vars and writes js/firebase-config.js.
 * 로컬: env가 없고 이미 firebase-config.js가 있으면 건너뜀.
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const outPath = path.join(root, "js", "firebase-config.js");

const ENV_MAP = {
  FIREBASE_API_KEY: "apiKey",
  FIREBASE_AUTH_DOMAIN: "authDomain",
  FIREBASE_PROJECT_ID: "projectId",
  FIREBASE_STORAGE_BUCKET: "storageBucket",
  FIREBASE_MESSAGING_SENDER_ID: "messagingSenderId",
  FIREBASE_APP_ID: "appId",
  FIREBASE_DATABASE_URL: "databaseURL",
};

const envKeys = Object.keys(ENV_MAP);
const missing = envKeys.filter((k) => !process.env[k]?.trim());

if (missing.length > 0) {
  if (fs.existsSync(outPath)) {
    console.log(
      "[write-firebase-config] 환경 변수 없음 — 기존 js/firebase-config.js 유지",
    );
    process.exit(0);
  }
  console.error(
    "[write-firebase-config] 필요한 환경 변수가 없습니다:",
    missing.join(", "),
  );
  console.error("Vercel 대시보드에 설정하거나, 로컬에서는 template을 복사하세요.");
  process.exit(1);
}

const config = {};
for (const [envKey, objKey] of Object.entries(ENV_MAP)) {
  config[objKey] = process.env[envKey].trim();
}

const fileContent = `/**
 * Auto-generated by scripts/write-firebase-config.mjs at build time. Do not edit.
 */
export const firebaseConfig = ${JSON.stringify(config, null, 2)};
`;

fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, fileContent, "utf8");
console.log("[write-firebase-config] 생성됨:", outPath);
